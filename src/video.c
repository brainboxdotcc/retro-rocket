void* backbuffer_copy_sse2(void *dest, const void *src, uint64_t len)
{
	uint32_t *d32 = dest;
	const uint32_t *s32 = src;

	while (((uintptr_t)d32 & 15) && len >= 4) {
		*d32++ = *s32++;
		len -= 4;
	}

	uint8_t *d = (uint8_t *)d32;
	const uint8_t *s = (const uint8_t *)s32;
	uint64_t blocks = len / 64;

	len %= 64;

	if (blocks) {
		__asm__ volatile(
			"1:\n"
			"movdqu   (%[s]), %%xmm0\n"
			"movdqu 16(%[s]), %%xmm1\n"
			"movdqu 32(%[s]), %%xmm2\n"
			"movdqu 48(%[s]), %%xmm3\n"
			"movdqa %%xmm0,   (%[d])\n"
			"movdqa %%xmm1, 16(%[d])\n"
			"movdqa %%xmm2, 32(%[d])\n"
			"movdqa %%xmm3, 48(%[d])\n"
			"add $64, %[s]\n"
			"add $64, %[d]\n"
			"dec %[blocks]\n"
			"jnz 1b\n"
			: [d] "+r"(d), [s] "+r"(s), [blocks] "+r"(blocks)
		:
		: "xmm0", "xmm1", "xmm2", "xmm3", "memory"
		);
	}

	d32 = (uint32_t *)d;
	s32 = (const uint32_t *)s;

	while (len >= 4) {
		*d32++ = *s32++;
		len -= 4;
	}

	return dest;
}

