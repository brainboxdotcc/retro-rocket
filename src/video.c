void init_console()
{
	if (framebuffer_request.response == NULL || framebuffer_request.response->framebuffer_count < 1) {
		dprintf("No framebuffer offered\n");
		wait_forever();
	}

	if (!cpu_caps.erms) {
		dprintf("No ERMS CPU caps: using SSE2 backbuffer copy.\n");
		backbuffer_copy_fn = backbuffer_copy_sse2;
	}

	rr_console_init_from_limine();

	screen_graphics_x = fb->width;
	screen_graphics_y = fb->height;

	clearscreen();

	dprintf("Bringing up flanterm...\n");

	ft_ctx = flanterm_fb_init(
		&kmalloc,
		&flanterm_free,
		rr_fb_back, fb->width, fb->height, fb->pitch,
		fb->red_mask_size, fb->red_mask_shift,
		fb->green_mask_size, fb->green_mask_shift,
		fb->blue_mask_size, fb->blue_mask_shift,
		NULL, // Background canvas
		NULL, // Ansi colours
		NULL, // Ansi bright colours
		NULL, // Default background
		NULL, // Default foreground
		NULL, // Default background bright
		NULL, // Default foreground bright
		font_data,
		8, // Font X size
		8, // Font Y size
		0, // Font vertical spacing
		1, // Font X Scale
		1, // Font Y Scale
		0  // Margin
	);
	if (!ft_ctx) {
		preboot_fail("Can't create flanterm fb");
	}
	dprintf("Flanterm address: %lx\n", (uint64_t)ft_ctx);
	flanterm_set_autoflush(ft_ctx, true);
	flanterm_set_callback(ft_ctx, terminal_callback);
	size_t x, y;
	flanterm_get_dimensions(ft_ctx, &x, &y);
	screen_x = x;
	screen_y = y;
	console_paging_reset();
	dprintf("Framebuffer address: %lx x resolution=%d y resolution=%d\n", framebuffer_address(), screen_get_width(), screen_get_height());

	setforeground(COLOUR_LIGHTYELLOW);
	printf("Retro-Rocket ");
	setforeground(COLOUR_WHITE);
	printf("64-bit SMP kernel booting\n");
	rr_flip();

	init_debug();
}

