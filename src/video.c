void console_paging_wait(void) {
	static const char prompt[] = "  Press any key for next page  ";

	if (!ft_ctx || !rr_fb_back || !console_paging_enabled) {
		return;
	}

	int64_t row_y = (screen_y - 1) * 8;
	if (row_y < 0) {
		return;
	}

	size_t row_bytes = rr_fb_pitch * 8;
	uint8_t *saved_row = kmalloc(row_bytes);
	if (!saved_row) {
		return;
	}

	memcpy(saved_row, (uint8_t *)rr_fb_back + (row_y * rr_fb_pitch), row_bytes);

	uint64_t cursor_x = 0;
	uint64_t cursor_y = 0;
	get_text_position(&cursor_x, &cursor_y);

	size_t prompt_len = strlen(prompt);
	int64_t prompt_x;
	if ((int64_t)screen_x > (int64_t)prompt_len) {
		prompt_x = screen_x - prompt_len + 1;
	} else {
		prompt_x = 1;
	}

	int64_t prompt_px = (prompt_x - 1) * 8;
	int64_t prompt_py = row_y;
	int64_t prompt_w = (int64_t)prompt_len * 8;

	draw_horizontal_rectangle(prompt_px, prompt_py, prompt_px + prompt_w, prompt_py + 8, 0xffffff);
	graphics_putstring(prompt, prompt_px, prompt_py, 0x000000);

	set_video_dirty_area(row_y, row_y + 8);
	rr_flip();

	interrupts_on();
	while (kgetc() == 255) {
		__asm__ volatile("hlt");
	}
	interrupts_off();

	memcpy((uint8_t *)rr_fb_back + (row_y * rr_fb_pitch), saved_row, row_bytes);
	flanterm_set_cursor_pos(ft_ctx, cursor_x, cursor_y);
	flanterm_flush(ft_ctx);

	set_video_dirty_area(row_y, row_y + 8);
	rr_flip();

	kfree(saved_row);
}

/* Insert a band [start, end), keeping the list sorted by start */
int add_scrollable(int64_t start, int64_t end) {
	if (scrollable_count >= MAX_SCROLLABLES) {
		return -1; /* list full */
	}

	int pos = 0;
	while (pos < scrollable_count && scrollables[pos].start < start) {
		pos++;
	}

	/* shift up to make room */
	for (int i = scrollable_count; i > pos; i--) {
		scrollables[i] = scrollables[i - 1];
	}

	scrollables[pos].start = start;
	scrollables[pos].end = end;
	scrollable_count++;

	return 0;
}

/* Remove a band matching [start, end) */
int remove_scrollable(int64_t start, int64_t end) {
	for (int i = 0; i < scrollable_count; i++) {
		if (scrollables[i].start == start && scrollables[i].end == end) {
			/* shift down */
			for (int j = i; j < scrollable_count - 1; j++) {
				scrollables[j] = scrollables[j + 1];
			}
			scrollable_count--;
			return 0;
		}
	}
	return -1; /* not found */
}

void rr_console_init_from_limine(void) {
	fb = framebuffer_request.response->framebuffers[0];
	dprintf("fb front: %lx\n", (uint64_t)fb->address);
	rr_fb_front  = fb->address;
	rr_fb_pitch  = fb->pitch;
	rr_fb_height = fb->height;
	bytes_per_pixel = fb->bpp >> 3;
	screen_graphics_stride = fb->pitch;
	rr_fb_bytes  = rr_fb_pitch * rr_fb_height;  // full bytes, includes padding per row
	rr_fb_back = kmalloc(rr_fb_bytes);
	if (!rr_fb_back) {
		preboot_fail("Out of memory for backbuffer");
	}
	memset(rr_fb_back, 0, rr_fb_bytes);
}

inline uint64_t framebuffer_address() {
	return (uint64_t)rr_fb_back;
}

inline uint64_t pixel_address(int64_t x, int64_t y) {
	if (x >= 0 && y >= 0 && x < screen_graphics_x && y < screen_graphics_y) {
		return (y * rr_fb_pitch) + (x * bytes_per_pixel);
	}
	return 0;
}

