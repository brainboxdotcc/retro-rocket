const char* ansi_colour(char *out, size_t out_len, unsigned char vga_colour, bool background) {
	unsigned char code;

	if (background) {
		code = map_vga_to_ansi_bg(vga_colour);
	} else {
		code = map_vga_to_ansi(vga_colour);
	}

	snprintf(out, out_len, "\x1b[%um", code);
	return out;
}

void setforeground(unsigned char foreground) {
	uint64_t flags;
	unsigned char ansi;
	size_t base;
	bool bright;

	ansi = map_vga_to_ansi(foreground);

	if (ansi >= 90) {
		bright = true;
		base = ansi - 90;
	} else {
		bright = false;
		base = ansi - 30;
	}

	lock_spinlock_irq(&console_spinlock, &flags);
	lock_spinlock(&debug_console_spinlock);
	flanterm_set_text_fg(ft_ctx, base, bright);
	unlock_spinlock(&debug_console_spinlock);
	unlock_spinlock_irq(&console_spinlock, flags);
}

void setbackground(unsigned char background) {
	uint64_t flags;
	unsigned char ansi;
	size_t base;
	bool bright;

	ansi = map_vga_to_ansi_bg(background);

	if (ansi >= 100) {
		bright = true;
		base = ansi - 100;
	} else {
		bright = false;
		base = ansi - 40;
	}

	lock_spinlock_irq(&console_spinlock, &flags);
	lock_spinlock(&debug_console_spinlock);

	flanterm_set_text_bg(ft_ctx, base, bright);

	unlock_spinlock(&debug_console_spinlock);
	unlock_spinlock_irq(&console_spinlock, flags);
}

